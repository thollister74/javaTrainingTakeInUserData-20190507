package src;

public class UserInformation {
    int userAge;
    int userHeight;
    String userFirstName;
    String userLastName;
    Color userHairColor;
    Color userEyeColor;

    public UserInformation(int age,
                           int height,
                           String fName,
                           String lName,
                           Color hairColor,
                           Color eyeColor) {
        this.userAge = age;
        this.userHeight = height;
        this.userFirstName = fName;
        this.userLastName = lName;
        this.userHairColor = hairColor;
        this.userEyeColor = eyeColor;


    }

}
